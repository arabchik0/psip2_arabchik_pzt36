<?php
// Задание 6: Пользовательская функция

echo "<h2>Пользовательская функция</h2>";

// Определение функции для расчетов по формуле
function calculateP($x, $y) {
    $result = log($x) - exp($x + $y) + ($x ** $y) / sqrt(2 + $y ** 2 + abs($x ** 3 - log($y)));
    return $result;
}

// Пример использования функции
$xValue = 10;
$yValue = 5;
$calculatedP = calculateP($xValue, $yValue);

echo "<p>Результат расчета P: $calculatedP</p>";

?>
